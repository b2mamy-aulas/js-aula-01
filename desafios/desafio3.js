// Crie um programa que receba um valor em dólares
// e o converta para reais.

// Taxa de câmbio: 5.30

// ENTRADA
// Receber valor em dólares
let numero1 = Number(prompt("Digite o valor em dólares USD"))
let taxaDeCambio = 5.30

// PROCESSAMENTO
// Fazer operação matemática
let resultado = numero1 * taxaDeCambio

//SAÍDA
// Exibir na tela o resultado
console.log(" O valor em " + numero1 + " USD referece a quantia de R$"  + resultado)
