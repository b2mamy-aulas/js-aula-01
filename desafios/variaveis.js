// Para declarar uma variável usamos o let. 
// O let precisa ser usado uma única vez, ok? Se quiser
// substituir o valor da variável nome, é só declarar o novo valor direto
// uma vez que ela já foi criada anteriormente <3 

// Variável do tipo string (string é o mesmo que texto)
let nome = "Flávia"

// O console.log é um comando de SAÍDA. 
console.log("Olá, " + nome + "!")

// Variável do tipo número.
let idade = 25

// Variável do tipoo booleano (verdadeiro ou falso).
let starWars = true

// O prompt serve para perguntar um dado para o usuário.
// Se esse dado for um número, não esqueça de converter com o Number :)
let numero = Number(prompt("Digite numero "))

// O alert serve para criar uma mensagem no navegador.
alert("SEU NAVEGADOR TRAVOU POR EXCESSO DE BELEZA.")